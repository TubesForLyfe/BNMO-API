package bnmo.bnmoapi.classes.users;

public class UserInfo {
    public String nama;
    public String username;
    public String image;
    public float saldo;

    public UserInfo(String nama, String username, String image, float saldo) {
        this.nama = nama;
        this.username = username;
        this.image = image;
        this.saldo = saldo;
    }
}
